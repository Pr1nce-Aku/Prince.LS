function searchLS() {
    let input = document.getElementById("searchInput");
    let searchValue = input.value.trim();
    let resultsDiv = document.getElementById("results");

    resultsDiv.innerHTML = "";

    if (searchValue === "") {
        resultsDiv.innerHTML = "Введите последние цифры ЛС.";
        return;
    }

    let foundLS = lsDatabase.filter(ls => ls.toString().endsWith(searchValue));

    if (foundLS.length > 0) {
        resultsDiv.innerHTML = "Найденные ЛС:<br>" + foundLS.join("<br>");
    } else {
        resultsDiv.innerHTML = "Нет совпадений.";
        input.value = "";
        setTimeout(() => {
            resultsDiv.innerHTML = "";
        }, 2000);
    }
}
